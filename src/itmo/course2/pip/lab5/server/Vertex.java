package itmo.course2.pip.lab5.server;


public class Vertex {
    private float x;
    private float y;

    public Vertex(float newX, float newY) {
        this.x = newX;
        this.y = newY;
    }

    public float getXCoord() {
        return this.x;
    }

    public float getYCoord() {
        return this.y;
    }
}

